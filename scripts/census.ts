function main(): void {
  console.log('Vietnamese Law MCP — Census');
  console.log('===========================\n');
  console.log('  Source:  vbpl.vn / thuvienphapluat.vn (cross-referenced)');
  console.log('  Method:  Curated master list + thuvienphapluat.vn URLs');
  console.log('  License: Government Open Data / Open Access\n');

  // Compute stats
  const byType: Record<string, number> = {};
  const byStatus: Record<string, number> = {};
  for (const law of VIETNAMESE_LAWS) {
    byType[law.type] = (byType[law.type] ?? 0) + 1;
    byStatus[law.status] = (byStatus[law.status] ?? 0) + 1;
  }

  // Check for duplicate IDs
  const ids = new Set<string>();
  for (const law of VIETNAMESE_LAWS) {
    if (ids.has(law.id)) {
      console.error(`  ERROR: Duplicate ID: ${law.id}`);
    }
    ids.add(law.id);
  }

  // Sort by type then date
  const sorted = [...VIETNAMESE_LAWS].sort((a, b) => {
    const typeOrder = { constitution: 0, code: 1, law: 2, ordinance: 3, decree: 4 };
    const ta = typeOrder[a.type] ?? 5;
    const tb = typeOrder[b.type] ?? 5;
    if (ta !== tb) return ta - tb;
    return a.issued_date.localeCompare(b.issued_date);
  });

  const census: CensusOutput = {
    generated_at: new Date().toISOString(),
    source: 'vbpl.vn / thuvienphapluat.vn (curated master list)',
    description: 'Census of Vietnamese national legislation: Constitution, Codes, Laws, key Ordinances, and PDP Decree',
    stats: {
      total: sorted.length,
      by_type: byType,
      by_status: byStatus,
      class_ingestable: sorted.filter(a => a.classification === 'ingestable').length,
      class_inaccessible: sorted.filter(a => a.classification === 'inaccessible').length,
      class_metadata_only: sorted.filter(a => a.classification === 'metadata_only').length,
    },
    laws: sorted,
  };

  fs.mkdirSync(DATA_DIR, { recursive: true });
  fs.writeFileSync(CENSUS_PATH, JSON.stringify(census, null, 2) + '\n');

  console.log(`${'='.repeat(50)}`);
  console.log('CENSUS COMPLETE');
  console.log('='.repeat(50));
  console.log(`  Total laws discovered: ${sorted.length}`);
  console.log(`  By type:`);
  for (const [type, count] of Object.entries(byType).sort()) {
    console.log(`    ${type.padEnd(15)} ${count}`);
  }
  console.log(`  By status:`);
  for (const [status, count] of Object.entries(byStatus).sort()) {
    console.log(`    ${status.padEnd(20)} ${count}`);
  }
  console.log(`  Ingestable:  ${census.stats.class_ingestable}`);
  console.log(`\n  Output: ${CENSUS_PATH}`);
}

main();
