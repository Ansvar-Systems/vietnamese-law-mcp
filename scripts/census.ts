/**
 * Vietnamese Law MCP — Census Script
 *
 * Enumerates ALL Vietnamese national legislation (Laws, Codes, Ordinances)
 * from the National Assembly and Standing Committee.
 *
 * Sources:
 *   - Curated master list based on vbpl.vn (National Legal Document Database)
 *   - Cross-referenced with thuvienphapluat.vn URLs
 *
 * Vietnamese legislation hierarchy (top-level only — what we ingest):
 *   - Hiến pháp (Constitution)
 *   - Bộ luật (Codes) — e.g., Bộ luật Hình sự, Bộ luật Dân sự
 *   - Luật (Laws) — passed by National Assembly (Quốc hội)
 *   - Pháp lệnh (Ordinances) — issued by Standing Committee
 *
 * We do NOT ingest sub-law instruments (Nghị định/Decrees, Thông tư/Circulars,
 * Quyết định/Decisions) except for Decree 13/2023/NĐ-CP (Personal Data
 * Protection) due to its standalone significance.
 *
 * Writes data/census.json in golden standard format.
 *
 * Usage:
 *   npx tsx scripts/census.ts
 */

